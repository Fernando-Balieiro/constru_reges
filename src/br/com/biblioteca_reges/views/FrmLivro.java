    private void cadLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadLivroActionPerformed

        Calendar dataAtual = Calendar.getInstance();

        Livro livro = new Livro(txtLivro.getText(),
           txtAutor.getText(),
           Integer.parseInt(txtQuantidade.getText()),
           Float.parseFloat(txtPreco.getText()),
           txtCategoria.getText(),
           dataAtual
        );

        livro.imprimir();
        
        //enviar para o banco de dados
        LivroRdn rdn = new LivroRdn();
        
        rdn.cadastrarLivro(livro);
        
        //limpar os componentes
        limparCampos();
    }//GEN-LAST:event_cadLivroActionPerformed

