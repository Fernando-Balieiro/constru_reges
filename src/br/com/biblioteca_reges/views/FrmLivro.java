    private void botPesquisaLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botPesquisaLivroActionPerformed
        
        LivroRdn livro = new LivroRdn();
        
        Livro resultSearch = livro.obterLivroPorNome(txtLivro.getText());
        
        if(resultSearch == null){
            System.out.println("Livro não encontrado no banco!");
        };
        
        txtLivro.setText(resultSearch.getNomeLivro());
        txtAutor.setText(resultSearch.getNomeAutor());
        txtQuantidade.setText(String.valueOf(resultSearch.getQuantidade()));
        txtPreco.setText(String.valueOf(resultSearch.getPreco()));
        txtCategoria.setText(resultSearch.getCategoria());
    }//GEN-LAST:event_botPesquisaLivroActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        LivroRdn livro = new LivroRdn();
        
        Livro livroModel = new Livro(txtLivro.getText(),
           txtAutor.getText(),
           Integer.parseInt(txtQuantidade.getText()),
           Float.parseFloat(txtPreco.getText()),
           txtCategoria.getText(),
           null
        );
        
        int livroId = livro.obterLivroIdPorNome(txtLivro.getText());
        boolean resultUpdate = livro.atualizarPorId(livroId, livroModel);
        
        if(resultUpdate){
            System.out.println("Livro Atualizado com sucesso!!");
        };
        
        limparCampos();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        LivroRdn livro = new LivroRdn();
        
        int livroId = livro.obterLivroIdPorNome(txtLivro.getText());
        boolean resultDelete = livro.deletarPorId(livroId);
        
        if(resultDelete){
            System.out.println("Livro deletado com sucesso!!");
        };
        
        limparCampos();
    }//GEN-LAST:event_jButton2ActionPerformed

