    private void tableAlunoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableAlunoMouseClicked
        // TODO add your handling code here:
        this.modoAlterarDeletar = true;

        //PEGA A LINHA SELECIONADA
        int row = this.tableAluno.getSelectedRow();

        //RECUPERA O VALOr DA COLUNA ID ESTA NA 0
        int idAluno = (int) this.tableAluno.getValueAt(row, 0);

        //GUARDA O ID PARA ALTERAR/REMOVER
        this.id = idAluno;

        //int indice = 0;
        AlunoRdn rdn = new AlunoRdn();

        //RECUPERAR POR ID
        Aluno aluno = rdn.obterPorId(id);

      
        txtAluno.setText(aluno.getNomeCompleto());
        txtSenha.setText(aluno.getAlunoSenha());
        txtRg.setText(aluno.getRgIe());
        txtEmail.setText(aluno.getEmail());
        txtTelefone.setText(aluno.getTelefone());
        txtDocumento.setText(aluno.getDocumento());

        //ALTERAR A DATA
        DateFormat formataData = new SimpleDateFormat("dd/MM/yyyy");
        txtNascimento.setText(formataData.format(aluno.getDataNascFund().getTime()));

        txtRua.setText(aluno.getEndereco().getLogradouro());
        txtNumero.setText(aluno.getEndereco().getNumero());
        txtBairro.setText(aluno.getEndereco().getBairro());
        txtCep.setText(aluno.getEndereco().getCep());

        txtUf.setText(aluno.getEndereco().getUf());
        txtCidade.setText(aluno.getEndereco().getCidade());

        this.habilitarCampos(true);

        btnNovo.setEnabled(false);

        btnExcluir.setEnabled(true);

        btnSalvar.setText("Alterar");
        
        btnSalvar.setEnabled(true);


    }//GEN-LAST:event_tableAlunoMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        int input = JOptionPane.showConfirmDialog(null,
                "Deseja realmente excluir?", "Atenção!!!", JOptionPane.YES_NO_OPTION);

        if (input == 0) {

            EnderecoRdn endRdn = new EnderecoRdn();

            endRdn.deletarEnderecoPorAluno(this.id);

            AlunoRdn rdn = new AlunoRdn();

            rdn.deletarAluno(this.id);

            this.carregarTabela();

        }
        this.modoNovo();
    }//GEN-LAST:event_btnExcluirActionPerformed

