    private void carregarTabela() {

        //CLASSE USADA PARA MANIPULAR A TABELA
        DefaultTableModel model = (DefaultTableModel) tableAluno.getModel();

        //limpar a tabela antes de selecionar
        model.setRowCount(0);

        AlunoRdn cliRdn = new AlunoRdn();

        List<Aluno> lstCli = cliRdn.obterTodos();

        //para cada aluno da lista
        if (lstCli != null) {
            for (Aluno cli : lstCli) {

                DateFormat formataData = new SimpleDateFormat("dd/MM/yyyy");

                //adicionar a linha
                model.addRow(new Object[]{
                    cli.getId(),
                    cli.getNomeCompleto(),
                    cli.getDocumento(),
                    cli.getTelefone(),
                    formataData.format(cli.getDataNascFund().getTime()),
                    cli.getEmail(),
                    cli.getEndereco().getLogradouro(),
                    cli.getEndereco().getBairro(),
                    cli.getEndereco().getCep(),
                    cli.getEndereco().getCidade(),
                    cli.getEndereco().getNumero(),
                    cli.getEndereco().getUf()
                });
            }
        }
        tableAluno.setModel(model);

    }
