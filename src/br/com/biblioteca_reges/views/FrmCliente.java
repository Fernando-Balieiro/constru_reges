    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        Calendar cal = Calendar.getInstance();

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        try {

            cal.setTime(sdf.parse(txtNascimento.getText()));
        } catch (ParseException ex) {
            throw new RuntimeException("Erro ao converter a data. Certifique-se de inserir a data no formato correto (dd/MM/yyyy).", ex);
        }

        //int id, String logradouro, String bairro, String numero, String cidade, String uf, String cep, int idAluno) {
        //carregar os objetos
        Endereco endereco = new Endereco(0, txtRua.getText(),
                txtBairro.getText(), txtNumero.getText(),
                txtCidade.getText(), txtUf.getText(), txtCep.getText(), this.id);

        Aluno aluno = new Aluno(this.id,
                Integer.parseInt(txtRaAluno.getText()),
                txtSenha.getText(),
                txtAluno.getText(),
                endereco,
                txtTelefone.getText(),
                cal,
                txtDocumento.getText(),
                txtRg.getText(),
                txtEmail.getText()
        );

        aluno.imprimir();

          AlunoRdn rdn = new AlunoRdn();
        if(this.modoAlterarDeletar == true){
        
            rdn.alterarAluno(aluno);
        }
        else{
        //enviar para o banco de dados
      
        rdn.inserirAluno(aluno);
        }
        //limpar os componentes
        limparCampos();

        //modo inicial
        modoInicial();

        //desabilitar componentes
        habilitarCampos(false);
        
        this.carregarTabela();


    }//GEN-LAST:event_btnSalvarActionPerformed


